// Named import
import { a } from "foo";

// Default import
import b from "foo";

// Namespace import
import * as c from "foo";

// Type import
import type { d } from "foo";

// Multiple named imports
import { e, f } from "foo";

// Multiple named imports with type import
import type { g, h } from "foo";
import { i, type j } from "foo";
