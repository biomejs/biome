impl HtmlSyntaxKind {
    /// Returns `true` if this kind can be the name of an [`crate::HtmlTagName`]: a
    /// known HTML/SVG tag-name keyword or the `HTML_UNKNOWN_TAG` fallback.
    pub fn is_html_tag_name(self) -> bool {
        HTML_TAG_NAMES.contains(self)
    }
}

#[cfg(test)]
mod tests {
    use crate::HtmlSyntaxKind;

    /// Sanity-check that the hand-maintained [`super::HTML_TAG_NAMES`] set stays in
    /// sync with the tag keywords: representative HTML/SVG tags (including ones that
    /// reuse an existing keyword kind) must be tag names, and non-tag keywords must
    /// not be. The spec-test fixtures provide broader coverage.
    #[test]
    fn tag_name_classification() {
        for tag in [
            "div",
            "span",
            "a",
            "svg",
            "circle",
            "feGaussianBlur",
            "html",
            "style",
            "use",
            "set",
            "animate",
        ] {
            let kind = HtmlSyntaxKind::from_keyword(tag)
                .unwrap_or_else(|| panic!("`{tag}` should be a keyword"));
            assert!(kind.is_html_tag_name(), "`{tag}` should be a tag name");
        }

        for non_tag in ["bind", "debug", "if", "each"] {
            let kind = HtmlSyntaxKind::from_keyword(non_tag)
                .unwrap_or_else(|| panic!("`{non_tag}` should be a keyword"));
            assert!(
                !kind.is_html_tag_name(),
                "`{non_tag}` should not be a tag name",
            );
        }
    }
}
